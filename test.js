function getCircleCoordinates(radius) {
    const coordinates = [];
    let x = 0;
    let y = 0;

    for (let i = 1; i <= radius; i++) {
        // 上边
        for (let j = -i; j <= i; j++) {
            coordinates.push([x - i, y + j]);
        }

        // 右边
        for (let j = -i + 1; j <= i; j++) {
            coordinates.push([x + j, y + i]);
        }

        // 下边
        for (let j = -i + 1; j <= i; j++) {
            coordinates.push([x + i, y - j]);
        }

        // 左边
        for (let j = -i + 1; j < i; j++) {
            coordinates.push([x - j, y - i]);
        }
    }

    return coordinates;
}

